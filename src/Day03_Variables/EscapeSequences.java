package Day03_Variables;


/*
    \n: new line (enter key)

    \t: paragraph space

    \" here \" double quote

    \\ to print backslash

     */


public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println(" Hello everyone\nHow are you all today");

        System.out.println("-----------------------------------");

        System.out.println("Hello my name is Karina\nI live in Virginia");

        System.out.println("--------------");
        System.out.println("\tToDAY IS A GOOD DAY TO STUDY JAVA");
        System.out.println("------------------------");


        System.out.println("My favorite is \"game of thrones\"");

        System.out.println("-----------");

        System.out.println("\\");


    }


}
