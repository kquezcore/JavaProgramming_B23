package Day23_Arrays;

public class TriangleLoop {
    public static void main(String[] args) {

        for (int j = 1; j <= 8; j++) {// j number of lines
            for (int i = 1; i <= j; i++) {// i number of abstracts
                System.out.print("*");
            }
            System.out.println();


        }
    }
}
